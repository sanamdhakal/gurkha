$suits = ["hearts", "diamonds", "clubs", "spades"];
$ranks = ["2", "3", "4", "5", "6", "7", "8", "9", "10", "jack", "queen", "king", "ace"];
$deck = [];

foreach ($suits as $suit) {
    foreach ($ranks as $rank) {
        $deck[] = $rank . " of " . $suit;
    }
}

// Shuffle the deck using the Fisher-Yates algorithm
function shuffleDeck(&$deck) {
    $count = count($deck);
    for ($i = $count - 1; $i > 0; $i--) {
        $j = mt_rand(0, $i);
        $temp = $deck[$i];
        $deck[$i] = $deck[$j];
        $deck[$j] = $temp;
    }
}

shuffleDeck($deck);

